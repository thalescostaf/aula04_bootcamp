produto1: str = "sapato"
produto2: str = "camiseta"
produto3: str = "videogame"

produtos: list = []

# Comando para adicionar uma variável na lista (append)

produtos.append(produto1)
produtos.append(produto2)
produtos.append(produto3)

print(produtos)

# Comando para retirar o último produto da lista (pop)

produtos.pop()

print(produtos)

